# << : left shift 연산자
#   4를 2진수를 바꾸면 100
#   그걸 왼쪽으로 3번 밀고
#   빈 자리에 0 채우고 -> 100000
#   그걸 10진수로 바꾸면 32
# >> : right shift
# <<< : rotation shift
x = 4
y = 3
z = x << y
print(z)

# 글 정렬
#   Left = 1
#   Center = 2
#   Right = 3
# 글 속성
#   이탤릭 = 1 << 0 (1)
#   굵게 = 1 << 1 (2)
#   밑줄 = 1 << 2 (4)

# 글.속성 = 5 = 1 << 0 + 1 << 2
#           1 + 4 => 이탤릭, 밑줄
