# CPU : 연산장치
# RAM : 저장장치
#   변수 형태로
#   임시 저장(다양한 루트로 휘발)
# SSD/HDD : 저장장치
#   파일 형태로
#   영구 보존(직접 지우지 않는다면)
# GPU : 그래픽 처리용(CPU + RAM + HDD)

# 변수(variable)
#   RAM에 생기는 데이터 저장하는 공간(그릇)
#   언제 사용? : 프로그램에서 발생한 데이터를
#               임시저장해야겠다

# 소스 한 글자 치면 프로그램 용량 2bytes씩 증가 => 한 글자라도 덜 치자
# 변수 만들면 RAM 사용량 증가 => 변수를 하나라도 덜 만들자

# 변수명 = 데이터
